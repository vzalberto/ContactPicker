package com.example.database;

public class ContactosNotificacionBean {
	
	
	private int idContacto;
	private String nombre;
	private String numero;
	private int notificacionSMS;
	private int notificacionApp; 
	private String idAppContacto;
	
	public int getIdContacto() {
		return idContacto;
	}
	public void setIdContacto(int idContacto) {
		this.idContacto = idContacto;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public int getNotificacionSMS() {
		return notificacionSMS;
	}
	public void setNotificacionSMS(int notificacionSMS) {
		this.notificacionSMS = notificacionSMS;
	}
	public int getNotificacionApp() {
		return notificacionApp;
	}
	public void setNotificacionApp(int notificacionApp) {
		this.notificacionApp = notificacionApp;
	}
	public String getIdAppContacto() {
		return idAppContacto;
	}
	public void setIdAppContacto(String idAppContacto) {
		this.idAppContacto = idAppContacto;
	}
	
	
	
	
	
	

}
